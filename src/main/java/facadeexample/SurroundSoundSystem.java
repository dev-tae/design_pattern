package main.java.facadeexample;

public class SurroundSoundSystem {
    public void on() {
        System.out.println("Surround Sound is turned on");
    }

    public void setVolume(int volume) {
        System.out.println("Volume is set to " + volume);
    }

    public void off() {
        System.out.println("Surround Sound is turned off");
    }
}
